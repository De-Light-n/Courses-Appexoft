"""Власні винятки застосунку.

Свої винятки роблять код зрозумілішим: за типом помилки одразу видно, що сталося.
Демонструє наслідування (усі — нащадки базового `TaskAppError`).
"""


class TaskAppError(Exception):
    """Базовий виняток застосунку. Від нього успадковуються всі інші."""


class TaskNotFoundError(TaskAppError):
    """Завдання із заданим id не знайдено."""
